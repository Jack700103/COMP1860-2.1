// SumEvenIntegers.asm
// 计算前n个偶数的和: z = 0 + 2 + 4 + ... + 2n
// 输入: R0 = n (不修改R0)
// 输出: R1 = 结果
// 特殊规则:
//   - n < 0 → R1 = -1
//   - 求和溢出 → R1 = -2
//   - 正常情况 → R1 = 求和结果

// ===================== 步骤1: 检查n是否为负数 =====================
@R0
D=M             // D = n
@NEGATIVE_CASE  // 若n < 0，跳转到负数处理分支
D;JLT

// ===================== 步骤2: 初始化变量 =====================
// 临时寄存器用途：
// R3: 循环计数器（初始值=n，递减到0）
// R4: 累加器（存储求和结果，初始值=0）
// R5: 临时存储当前要累加的偶数（2*i）
@R0
D=M
@R3
M=D             // R3 = n (循环计数器)
@R4
M=0             // R4 = 0 (累加器初始化为0)

// ===================== 步骤3: 循环求和 =====================
(LOOP)
// 终止条件：计数器 <= 0 时退出循环
@R3
D=M
@CHECK_OVERFLOW // 计数器耗尽，跳转到溢出检查
D;JLT

// 计算当前要累加的偶数: 2 * (n - 计数器) → 等价于 2*i（i从0到n）
// 简化逻辑：直接累加 2*计数器（因为 i 从0到n，等价于累加 2*0 + 2*1 + ... + 2*n）
@R3
D=M             // D = 当前计数器值
D=D+D           // D = 2 * 计数器 (当前要累加的偶数)
@R5
M=D             // R5 = 2*i

// 累加前先检查溢出（复用第5题溢出判断逻辑：同号相加且结果符号相反则溢出）
@R4
D=M             // D = 当前累加和
@R5
D=D+M           // 预计算: 累加和 + 当前偶数
@TEMP_SUM
M=D             // 临时存储预计算结果

// 溢出检查逻辑：
// 1. 累加器（R4）和当前偶数（R5）同号；
// 2. 预计算结果（TEMP_SUM）与累加器符号相反 → 溢出
@R4
D=M             // D = 累加和
@SIGN_ACC       // 存储累加和的符号
M=D
@R5
D=M             // D = 当前偶数
@SIGN_CURRENT   // 存储当前偶数的符号
M=D

// 检查累加器和当前偶数是否同号
@SIGN_ACC
D=M
@SIGN_CURRENT
D=D&M           // 同号则符号位一致，异号则为0
@DIFF_SIGN      // 符号不同，无溢出，跳转到累加步骤
D;JEQ

// 符号相同，检查预计算结果是否符号相反
@TEMP_SUM
D=M
@SIGN_ACC
D=D&M           // 若结果符号与累加器相反 → 溢出
@OVERFLOW_CASE  // 跳转到溢出处理分支
D;JEQ

// ===================== 步骤4: 执行累加 =====================
(DIFF_SIGN)
@TEMP_SUM
D=M
@R4
M=D             // 累加器 = 累加器 + 当前偶数

// 计数器减1
@R3
M=M-1
@LOOP           // 跳回循环开头
0;JMP

// ===================== 步骤5: 溢出检查（循环结束后最终验证） =====================
(CHECK_OVERFLOW)
// 循环结束后，再次确认累加结果无溢出（双重保险）
@R4
D=M
@SET_RESULT     // 无溢出，跳转到结果赋值
0;JMP

// ===================== 分支1: 处理n为负数的情况 =====================
(NEGATIVE_CASE)
@R1
M=-1            // R1 = -1
@END
0;JMP

// ===================== 分支2: 处理溢出的情况 =====================
(OVERFLOW_CASE)
@R1
M=-2            // R1 = -2
@END
0;JMP

// ===================== 分支3: 正常情况，赋值结果 =====================
(SET_RESULT)
@R4
D=M
@R1
M=D             // R1 = 累加器（求和结果）

// ===================== 程序结束 =====================
(END)
@END
0;JMP

// 临时标签（仅用于存储中间值，无执行逻辑）
(TEMP_SUM)
(SIGN_ACC)
(SIGN_CURRENT)